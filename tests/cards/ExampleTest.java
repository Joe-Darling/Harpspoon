package cards;

/**
 * This test provides an example of how to write a test class.  Each test is
 * implemented as a static method.  The main method calls all of the test
 * methods.
 *
 * Created by robertstjacquesjr on 6/7/17.
 */
public class ExampleTest {
    /**
     * An example test  method.  A method like this should be created for
     * each new test.
     */
    public static void testOne() {
        System.out.println("Running test one.");
    }

    /**
     * An example test  method.  A method like this should be created for
     * each new test.
     */
    public static void testTwo() {
        System.out.println("Running test two.");
    }

    /**
     * The main method; should call each of the tests in the class.
     */
    public static void main(String[] args) {
        testOne();
        testTwo();
    }
}
